#define _GNU_SOURCE
#include <stdio.h>
#include <string.h>
#include <sys/mman.h>
#include <unistd.h>

static unsigned char shellcode[] = {
    0x31, 0xc0, 0xb0, 0x01, 0x31, 0xff, 0xff, 0xc7, 0x48, 0x8d, 0x35, 0x07,
    0x00, 0x00, 0x00, 0x31, 0xd2, 0xb2, 0x24, 0x0f, 0x05, 0xc3, 0x68, 0x65,
    0x6c, 0x6c, 0x6f, 0x77, 0x6f, 0x72, 0x6c, 0x64, 0x20, 0x66, 0x72, 0x6f,
    0x6d, 0x20, 0x6d, 0x6d, 0x61, 0x70, 0x20, 0x52, 0x57, 0x58, 0x20, 0x73,
    0x68, 0x65, 0x6c, 0x6c, 0x63, 0x6f, 0x64, 0x65, 0x21, 0x0a
};

int main(void) {
    size_t len = sizeof(shellcode);

    void *mem = mmap(NULL, len, PROT_READ | PROT_WRITE | PROT_EXEC,
                     MAP_PRIVATE | MAP_ANONYMOUS, -1, 0);
    if (mem == MAP_FAILED) {
        perror("mmap");
        return 1;
    }

    memcpy(mem, shellcode, len);
    ((void (*)(void))mem)();

    munmap(mem, len);
    return 0;
}
