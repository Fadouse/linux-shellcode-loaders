#define _GNU_SOURCE
#include <stdint.h>
#include <stdio.h>
#include <sys/mman.h>
#include <unistd.h>

#define SHELLCODE_PAGE_SIZE 4096UL

__attribute__((aligned(SHELLCODE_PAGE_SIZE)))
static unsigned char shellcode_page[SHELLCODE_PAGE_SIZE] = {
    0x31, 0xc0, 0xb0, 0x01, 0x31, 0xff, 0xff, 0xc7, 0x48, 0x8d, 0x35, 0x07,
    0x00, 0x00, 0x00, 0x31, 0xd2, 0xb2, 0x31, 0x0f, 0x05, 0xc3, 0x68, 0x65,
    0x6c, 0x6c, 0x6f, 0x77, 0x6f, 0x72, 0x6c, 0x64, 0x20, 0x66, 0x72, 0x6f,
    0x6d, 0x20, 0x67, 0x6c, 0x6f, 0x62, 0x61, 0x6c, 0x20, 0x61, 0x72, 0x72,
    0x61, 0x79, 0x20, 0x6d, 0x70, 0x72, 0x6f, 0x74, 0x65, 0x63, 0x74, 0x20,
    0x73, 0x68, 0x65, 0x6c, 0x6c, 0x63, 0x6f, 0x64, 0x65, 0x21, 0x0a
};

int main(void) {
    long page_size = sysconf(_SC_PAGESIZE);
    if (page_size <= 0) {
        perror("sysconf");
        return 1;
    }

    uintptr_t page_mask = ~((uintptr_t)page_size - 1U);
    void *page_start = (void *)((uintptr_t)shellcode_page & page_mask);

    if (mprotect(page_start, (size_t)page_size, PROT_READ | PROT_EXEC) != 0) {
        perror("mprotect");
        return 1;
    }

    ((void (*)(void))shellcode_page)();
    return 0;
}
